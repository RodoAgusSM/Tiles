    private void btnJugarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnJugarActionPerformed
        Jugador jugador1 = (Jugador) this.jComboBoxJugador1.getSelectedItem();
        Jugador jugador2 = (Jugador) this.jComboBoxJugador2.getSelectedItem();
        if (jugador1.equals(jugador2)) {
            JOptionPane.showMessageDialog(this, "Ya se ha seleccionado el jugador", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                if (txtFichasInvertidas.getText().trim().length() == 0) {
                    JOptionPane.showMessageDialog(this, "No ha seleccionado la cantidad de fichas invertidas", "Error", JOptionPane.ERROR_MESSAGE);
                } else {
                    if (parseInt(txtFichasInvertidas.getText().trim()) < 1 || parseInt(txtFichasInvertidas.getText().trim()) > 6) {
                        JOptionPane.showMessageDialog(this, "Cantidad invalida de fichas invertidas", "Error", JOptionPane.ERROR_MESSAGE);
                    } else {
                        Partida partida = new Partida(jugador1, jugador2, parseInt(txtFichasInvertidas.getText()));
                        miSistema.agregarPartida(partida);
                        TableroJ unaVJ = new TableroJ(miSistema, menu);
                        Dimension pantalla = Toolkit.getDefaultToolkit().getScreenSize();
                        Dimension ventana = unaVJ.getSize();
                        unaVJ.setLocation((pantalla.width - ventana.width) / 2, (pantalla.height - ventana.height) / 2);
                        unaVJ.setVisible(true);
                        unaVJ.setLocationRelativeTo(null);
                        unaVJ.pack();
                        this.dispose();
                    }
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Error reingrese la cantidad de fichas invertidas");
            }
        }
    }//GEN-LAST:event_btnJugarActionPerformed


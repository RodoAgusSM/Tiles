    private void actualizarListaDeJugadores() {
        Jugador[] jugadores = new Jugador[this.getMiSistema().getListaJugadores().size()];
        for (int i = 1; i <= this.getMiSistema().getListaJugadores().size(); i++) {
            Jugador unJugador = this.getMiSistema().getListaJugadores().get(i - 1);
            jugadores[i - 1] = unJugador;
        }
        DefaultComboBoxModel comboModel = new DefaultComboBoxModel(jugadores);
        jComboBoxJugador1.setModel(comboModel);
    }


 private void actualizarIngresos() {
        int ancho = unS.getPrecarga().getTableroFichas().length;
        int largo = unS.getPrecarga().getTableroFichas()[0].length;
        for (int m = 0; m < ancho; m++) {
            for (int j = 0; j < largo; j++) {
                if (unS.getPrecarga().hayFicha(m, j)) {
                    Ficha ficha = unS.getPrecarga().getTableroFichas()[m][j];
                    String letraFicha = unS.getPrecarga().getFicha(m, j);
                    if (letraFicha.contains("B")) {
                        ficha.setC(Color.WHITE);
                        botones[m][j].setBackground(ficha.getC());
                    } else {
                        if (letraFicha.contains("R")) {
                            ficha.setC(Color.RED);
                            botones[m][j].setBackground(ficha.getC());
                        }
                    }
                }
            }
        }
    }

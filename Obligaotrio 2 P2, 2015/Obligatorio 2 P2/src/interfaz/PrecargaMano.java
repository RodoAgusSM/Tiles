    public boolean aumento() {
        boolean ok = false;
        String color = "B";
        if (i == 0) {
            unS.getPrecarga().limpiar();
        }
        if (i < 12) {
            if (i < 5) {
                txtColor.setText("Blanca");
            } else {
                if (i == 5) {
                    txtColor.setText("Roja");
                } else {
                    color = "R";
                    txtColor.setText("Roja");
                }
            }
            String tot = jTextFieldCoordenada.getText();
            if (jTextFieldCoordenada.getText().length() < 2 || jTextFieldCoordenada.getText().length() > 3) {
                JOptionPane.showMessageDialog(this, "ERROR COORDENADA INVALIDA");
            } else {
                String fila = tot.substring(0, 1);
                String columna = tot.substring(1);
                boolean sePudo = unS.agregarFichaPrecargaMano(fila, columna, color);
                if (sePudo) {
                    i++;
                    this.actualizarIngresos();
                } else {
                    JOptionPane.showMessageDialog(this, "ERROR COORDENADA INVALIDA");
                }
            }
        }
        if (i == 12) {
            ok = true;
            btnMeter1.setEnabled(false);
        }
        return ok;
    }

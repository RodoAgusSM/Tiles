    private void btnprecargaTextoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnprecargaTextoActionPerformed
        try {
            Tablero tablero = (Tablero) jComboBoxListaTableros.getSelectedItem();
            if (tablero != null) {
                miSistema.modificarTableroPrecarga(tablero.copia());
                menu.setEnabled(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Seleccione un tablero");
            }
        } catch (ClassCastException ex) {
            JOptionPane.showMessageDialog(this, "Seleccione un tablero");
        }
    }//GEN-LAST:event_btnprecargaTextoActionPerformed


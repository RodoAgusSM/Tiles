    private void btnCargaArchivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargaArchivoActionPerformed
        File archivo = precargaFichaTxt();
        if (archivo != null) {
            String path = archivo.getAbsolutePath();
            if (path != null && !path.equals("")) {
                try {
                    Tablero tablero = LevantarTablero.leerTablero(path);
                    tablero.setNombre(archivo.getName());
                    tablero.setFecha(new Date());
                    miSistema.modificarTableroPrecarga(tablero);
                    miSistema.agregarTablero(tablero.copia());
                    JOptionPane.showMessageDialog(this, "Levantado");
                    menu.setEnabled(true);
                    this.dispose();
                } catch (FileNotFoundException ex) {
                    JOptionPane.showMessageDialog(this, "No existe el archivo");
                } catch (IOException ex) {
                    Logger.getLogger(Precarga.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_btnCargaArchivoActionPerformed


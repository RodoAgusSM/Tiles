    private void RankingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RankingActionPerformed
        if (unSistema.getListaJugadores().isEmpty()) {
            JOptionPane.showMessageDialog(this, "No se cumplen los requisitos necesarios para mostrar el ranking", "Error", JOptionPane.OK_CANCEL_OPTION);
        } else {
            Ranking cJ = new Ranking(this);
            cJ.setLocationRelativeTo(null);
            cJ.setVisible(true);
            Jugador p;
            int suma = unSistema.getListaJugadores().size();
            for (int i = 0; i < unSistema.getListaJugadores().size(); i++) {
                p = (Jugador) unSistema.getListaJugadores().get(i);
                suma = suma + p.getPartidasJugadas();
            }
            for (int j = suma; j >= 0; j--) {
                for (int i = 0; i < unSistema.getListaJugadores().size(); i++) {
                    p = (Jugador) unSistema.getListaJugadores().get(i);
                    if (p.getPartidasGanadas() == j) {
                        cJ.mostrarTabla(p);
                    }
                }
            }
        }

    }//GEN-LAST:event_RankingActionPerformed

